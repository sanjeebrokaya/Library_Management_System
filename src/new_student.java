    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
         try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/lms", "root", "");
            String sql = "insert into students"
                    +"(student_id, Name, Course, Year, Semester) "
                    + "values (?,?,?,?,?)";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, jTextField1.getText());
            ps.setString(2, jTextField2.getText());
            ps.setString(3, (String) jComboBox1.getSelectedItem());
            ps.setString(4, (String) jComboBox2.getSelectedItem());
            ps.setString(5, (String) jComboBox3.getSelectedItem());         
            ps.execute();
            JOptionPane.showMessageDialog(null, "New Student added");
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

